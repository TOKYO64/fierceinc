			<h2 class="colorPrimary">Research shows that better conversations reduce malpractice risk</h2>
			
		  <p>At Fierce, we believe that a single conversation can have far-reaching impact. This premise holds true in the world of healthcare, as indicated by studies such as one conducted by Dr. Wendy Levinson, Chair of the Department of Medicine at the University of Toronto. Her study, <i>Physician-patient communication: The relationship with malpractice claims among primary care physicians and surgeons</i> (JAMA, Vol. 277 No. 7, February 19, 1997) examined hundreds of conversations between a group of physicians and their patients. Those physicians who spent more quality time with their patients were not sued for malpractice. The tone and content of those discussions were what mattered most.<br /><br />
		    A July 2008 report issued by Press Ganey,<i> Reduce Liability Risk by Improving Your Patient Satisfaction,</i> similarly shows that patient dissatisfaction—contributed to by factors such as poor communication and lack of disclosure -- is the most accurate indicator of whether a patient will sue their physician. This issue is of such recent interest that the June 2009 New York Times article <i>If All Doctors Had More Time to Listen</i> discusses how some doctors are attempting to reinvent healthcare through "patient-centered practices" that allow them to emphasize prevention and education. Patients in such practices are more likely to say they received the care they needed.<br /><br />
		    The idea that better patient conversations can lead to improved relationships has been discussed for more than a decade. So how does one learn the mindset and skill set that's needed to have conversations that will make us all healthier?  <br /><br />
		    Fierce's methodology revolves around the very idea that the conversation is the relationship, and the relationship impacts the results. Based on the principles of Susan Scott's best-seller, <i>Fierce Conversations: Achieving Success in Work &amp; in Life, One Conversation at a Time,</i> this foundational, hands-on workshop will introduce you to the transformational ideas and principles that will shift your basic understanding of conversations and the power they hold in relationships, leadership, and results.  <br /><br />
		    We have helped healthcare leaders such as the University of Washington School of Medicine, Harborview Medical Center and Group Health Cooperative transform the conversations central to their success.  <br /><br />
